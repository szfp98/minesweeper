package minefield;

/**
 *
 */
public class Size {
    private final int x;
    private final int y;

    /**
     * @param x
     * @param y
     */
    public Size(int x, int y){
        this.x=x;
        this.y=y;
    }

    /**
     * @return
     */
    public int getX() {
        return x;
    }

    /**
     * @return
     */
    public int getY() {
        return y;
    }
}
